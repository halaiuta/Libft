#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new;
	t_list	*tmp;
	t_list	*fed;

	new = NULL;
	if (!lst || !f || !del)
		return (NULL);
	while (lst)
	{
		fed = f(lst->content);
		tmp = ft_lstnew(fed);
		if (!tmp)
		{
			del(fed);
			ft_lstclear(&new, del);
			return (NULL);
		}
		ft_lstadd_back(&new, tmp);
		lst = lst->next;
	}
	return (new);
}
